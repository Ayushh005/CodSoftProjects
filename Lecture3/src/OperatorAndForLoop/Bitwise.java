package OperatorAndForLoop;

public class Bitwise {
    public static void main(String[] args) {
        System.out.println(2 & 3);    // and gate
        System.out.println(2 | 3);    // or gate
        System.out.println(2 ^ 3);    // xor gate
        System.out.println(~2);       // not gate
        System.out.println(34<<1);    //left shift
        System.out.println(34>>2);    //right shift
        System.out.println(-4<<1);
        System.out.println(-4>>1);
    }
}
